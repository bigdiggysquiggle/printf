#include "libft.h"

char	*ft_strupper(char *s)
{
	char	*n;
	int		i;

	n = s;
	i = 0;
	while (n[i])
	{
		if ('a' <= n[i] && n[i] <= 'z')
			n[i] = ft_toupper(n[i]);
		i++;
	}
	return (n);
}
