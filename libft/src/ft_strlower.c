#include "libft.h"

char	*ft_strlower(char *s)
{
	char	*n;
	int		i;

	n = s;
	i = 0;
	while (n[i])
	{
		if ('A' <= n[i] && n[i] <= 'Z')
			n[i] = ft_tolower(n[i]);
		i++;
	}
	return (n);
}
